package com.cea.ehm.bean;

import java.io.Serializable;

/**
 * data_idplug（IDPLUG数据）
 */
public class DataIdPlug implements Serializable {
	private static final long serialVersionUID = 1L;

	private Integer id;
	/**
	 * 发动机ID
	 */
	private Integer engId;
	/**
	 * 发动机设备号
	 */
	private String engPn;
	/**
	 * 发动机型号
	 */
	private String engModel;
	/**
	 * 转速
	 */
	private String rating;
	/**
	 * 构型
	 */
	private String config;
	/**
	 * 飞机型号
	 */
	private String planeType;
	private String idPlugPn;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getEngId() {
		return engId;
	}

	public void setEngId(Integer engId) {
		this.engId = engId;
	}

	public String getEngPn() {
		return engPn;
	}

	public void setEngPn(String engPn) {
		this.engPn = engPn;
	}

	public String getEngModel() {
		return engModel;
	}

	public void setEngModel(String engModel) {
		this.engModel = engModel;
	}

	public String getRating() {
		return rating;
	}

	public void setRating(String rating) {
		this.rating = rating;
	}

	public String getConfig() {
		return config;
	}

	public void setConfig(String config) {
		this.config = config;
	}

	public String getPlaneType() {
		return planeType;
	}

	public void setPlaneType(String planeType) {
		this.planeType = planeType;
	}

	public String getIdPlugPn() {
		return idPlugPn;
	}

	public void setIdPlugPn(String idPlugPn) {
		this.idPlugPn = idPlugPn;
	}

}
