package com.cea.ehm.bean;

import java.io.Serializable;

/**
 * 水洗表
 */
public class WaterWash implements Serializable {
	private static final long serialVersionUID = 1L;
	// ID
	private Integer id;
	// 设备参数
	private String parameterApparatus;
	// 水洗人员
	private Integer userId;
	//水洗人员姓名
	private String userName;
	// 地理位置
	private String geographic;
	// 天气
	private String weather;
	//发动机ID
	private Integer engId;

	// 发动机型号
	private String engSn;
	// 效果
	private String result;
	// 评级
	private String level;
	// 持续时间
	private String durationTime;
	// 创建时间时间
	private String ctime;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getParameterApparatus() {
		return parameterApparatus;
	}
	public void setParameterApparatus(
			String parameterApparatus) {
		this.parameterApparatus = parameterApparatus;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getGeographic() {
		return geographic;
	}
	public void setGeographic(String geographic) {
		this.geographic = geographic;
	}
	public String getWeather() {
		return weather;
	}
	public void setWeather(String weather) {
		this.weather = weather;
	}
	public Integer getEngId() {
		return engId;
	}
	public void setEngId(Integer engId) {
		this.engId = engId;
	}
	public String getEngSn() {
		return engSn;
	}
	public void setEngSn(String engSn) {
		this.engSn = engSn;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getDurationTime() {
		return durationTime;
	}
	public void setDurationTime(String durationTime) {
		this.durationTime = durationTime;
	}
	public String getCtime() {
		return ctime;
	}
	public void setCtime(String ctime) {
		this.ctime = ctime;
	}
	
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	
	
}
